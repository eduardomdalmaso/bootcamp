# TIPOS DE OPERADORES COM PYTHON
# Precedência dos operadores
# {() > exp}
# {* > /} Ordem de execução esquerda para a direita
# {+ > -} Ordem de execução esquerda para a direita
prod_1 = 20
prod_2 = 10

print(prod_1 + prod_2)
print(prod_1 - prod_2)
print(prod_1 / prod_2)
print(prod_1 // prod_2)
print(prod_1 * prod_2)
print(prod_1 % prod_2)
print(prod_1 ** prod_2)